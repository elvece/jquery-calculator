//javaScript calculator functions

function sum (num1, num2){
  var answer = num1 + num2;
  return answer;
}

function subtract (num1, num2){
  var answer = num1 - num2;
  return answer;
}

function multiply (num1, num2){
  var answer = num1 * num2;
  return answer;
}

function divide (num1, num2){
  var answer = num1 / num2;
  return answer;
}

